select Д.Наименование, count(DISTINCT ФК.[Код формы контроля]) as 'Кол-во'
from dbo.Дисциплины Д inner join dbo.[Дисциплина в семестре] ДС
on Д.[Код дисциплины] = ДС.[Код дисциплины]
inner join dbo.[Формы контроля] ФК
on ФК.[Код формы контроля] = ДС.[Код формы контроля]
GROUP BY Д.Наименование
HAVING count(DISTINCT ФК.[Код формы контроля]) > 1