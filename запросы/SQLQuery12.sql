CREATE PROCEDURE Уменьшение_На_5 AS
UPDATE [Дисциплина в семестре] 
SET [Количество часов в неделю]  = [Количество часов в неделю] - 5
FROM [Дисциплина в семестре] dvs
INNER JOIN Группы gr ON gr.[ID группы] = dvs.[ID группы]
INNER JOIN Институты ON gr.[ID института] = Институты.[ID института]
INNER JOIN Дисциплины ON dvs.[Код дисциплницы] = Дисциплины.[Код дисциплины]
WHERE Институты.Наименование = 'ИК' AND Дисциплины.Наименование = 'Введение в информационные технологии'
